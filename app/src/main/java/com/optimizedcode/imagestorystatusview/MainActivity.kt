@Composable
fun WidgetDemo() {

    var text by remember {
        mutableStateOf("Auto mode on")
    }

    var pausedText by remember {
        mutableStateOf("In Resumed state")
    }

    val list = mutableListOf<List<Any>>()
    list.add(prepareSmallList())
    list.add(prepareMediumList())
    list.add(prepareLargeList())
    list.add(prepareSmallList())
    list.add(prepareSmallList())
    list.add(prepareSmallList())
    list.add(prepareSmallList())
    list.add(prepareSmallList())


    // Single widget view
    Text(text = text)
    Text(text = pausedText)
    DrawStatusThumbnailWithViewProgress(
        progressSize = 160,
        strokeActiveColor = colorResource(id = R.color.orange),
        imagesList = prepareSmallList(),
        strokeInActiveColor = Color.LightGray,
        progressBarColor = colorResource(id = R.color.orange),
        widgetBackground = MaterialTheme.colorScheme.surface,
        onAutoModeOffCallback = {
            text = "Auto mode off now"
        },
        onPausedProgressCallback = {
            pausedText = if (it) {
                "In Paused state"
            } else {
                "In Resumed state"
            }
        }
    )

    // Multiple widgets in horizontal row
    Text(text = "Multiple widgets in horizontal list")
    HorizontalList(list)

    // Multiple widgets in Vertical row
    Text(text = "Multiple widgets in column list")
    VerticalList(list)
}

@Composable
fun HorizontalList(list: MutableList<List<Any>>) {
    LazyRow(
        modifier = Modifier.fillMaxWidth()
    ) {
        items(items = list) { item ->
            DrawStatusThumbnailWithViewProgress(
                progressSize = 70,
                strokeActiveColor = colorResource(id = R.color.purple_500),
                imagesList = item,
                autoMode = false,
                strokeInActiveColor = Color.LightGray,
                progressBarColor = colorResource(id = R.color.purple_200),
                widgetBackground = MaterialTheme.colorScheme.surface
            )
        }
    }
}

data class Description(
    val name: String, val time: String
)

@Composable
fun VerticalList(list: MutableList<List<Any>>) {

    val descriptionList = mutableListOf<Description>()
    descriptionList.add(Description("John kennedy", "10:45 PM"))
    descriptionList.add(Description("Shawn", "09:01 PM"))
    descriptionList.add(Description("Wasim akram", "06:33 PM"))
    descriptionList.add(Description("Shaheen afridi", "12:21 AM"))
    descriptionList.add(Description("William jack", "02:19 PM"))
    descriptionList.add(Description("David", "01:00: PM"))
    descriptionList.add(Description("George", "01:20 PM"))
    descriptionList.add(Description("James daniel", "01:15 PM"))

    LazyColumn(
        modifier = Modifier.fillMaxWidth()
    ) {
        itemsIndexed(items = list) {index, item ->
            Row(
                modifier = Modifier
                    .fillMaxWidth()
                    .height(80.dp),
                verticalAlignment = Alignment.CenterVertically,
                horizontalArrangement = Arrangement.spacedBy(15.dp)
            ) {
                DrawStatusThumbnailWithViewProgress(
                    progressSize = 70,
                    strokeActiveColor = colorResource(id = R.color.purple_500),
                    imagesList = item,
                    autoMode = false,
                    strokeInActiveColor = Color.LightGray,
                    progressBarColor = colorResource(id = R.color.purple_200),
                    widgetBackground = MaterialTheme.colorScheme.surface
                )

                Column {
                    Text(text = descriptionList[index].name)
                    Text(text = descriptionList[index].time)
                }
            }
        }
    }
}

